CASEID,
CRRPTNO,
CASE WHEN LENGTH(QRYIDBAN) = 8 THEN SDG.UFN_MASK_BAN(QRYIDBAN) WHEN LENGTH(QRYIDBAN) = 10 THEN SDG.UFN_MASK_IDN(QRYIDBAN) END,
