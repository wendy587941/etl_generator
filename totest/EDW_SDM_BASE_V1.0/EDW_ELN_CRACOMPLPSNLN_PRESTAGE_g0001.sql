LPSN_DEPART,
CASE WHEN LENGTH(LPSN_PID) = 8 THEN SDG.UFN_MASK_BAN(LPSN_PID) WHEN LENGTH(LPSN_PID) = 10 THEN SDG.UFN_MASK_IDN(LPSN_PID) END,
LPSN_BRNO,
SDG.UFN_MASK_INFO(LPSN_POSNO),
SDG.UFN_MASK_NM(LPSN_NAME),
CURRENT_TIMESTAMP,
to_date('${BD_EXCH_DATE}', 'yyyy-MM-dd')
FROM ${SOURCE_TABLE_OWNER}.${SOURCE_TABLE}
WHERE ${DATA_LIFE_BASE} = to_date('${BD_EXCH_DATE}', 'yyyy-MM-dd')
