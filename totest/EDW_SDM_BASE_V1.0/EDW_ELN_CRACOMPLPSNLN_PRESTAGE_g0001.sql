(
CASEID,
CRRPTNO,
QRYIDBAN,
LPSN_DATE,
LPSN_DEPART,
LPSN_PID,
LPSN_BRNO,
LPSN_POSNO,
LPSN_NAME,
DATA_UPDATE_TIME,
DATA_EXCH_DATE
